/**
 * op_add - suma dos valores
 * @a: 1er valor
 * @b: 2do valor
 *
 * Return: retorna el resultado
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - resta dos valores
 * @a: 1er valor
 * @b: 2do valor
 *
 * Return: retorna el resultado
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplica dos valores
 * @a: 1er valor
 * @b: 2do valor
 *
 * Return: retorna el resultado
 */

int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide dos valores
 * @a: 1er valor
 * @b: 2do valor
 *
 * Return: retorna el resultado
 */

int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calcula el resto de dos valores
 * @a: 1er valor
 * @b: 2do valor
 *
 * Return: retorna el resultado
 */
int op_mod(int a, int b)
{
	return (a % b);
}
